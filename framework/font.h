constexpr int Font_kSize	  = 16;
constexpr int Font_kHeight	  = 12;
constexpr int Font_kSpacing	  = 10;
constexpr int Font_kFirstChar = ' ';
constexpr int Font_kLastChar  = '~';
constexpr int Font_kBplWidth  = 256;
constexpr int Font_kBplHeight = 96;
constexpr int Font_kBplPlanes = 2;
constexpr int Font_kBplPitch  = Font_kBplWidth / 8 * Font_kBplPlanes;

////////////////////////////////////////////////////////////////////////////////
// textBpl1	 - pointer to first text draw area bitplane.
// textBpl2	 - pointer to second text draw area bitplane.
// textPitch - byte count between text draw area lines.
// fontBpls	 - pointer to font bitplanes.
////////////////////////////////////////////////////////////////////////////////
void Font_Init(u16* textBpl1, u16* textBpl2, s16 textPitch, const u16* fontBpls);
void Font_Deinit();

////////////////////////////////////////////////////////////////////////////////
// textBpl1 - pointer to first text draw area bitplane.
// textBpl2 - pointer to second text draw area bitplane.
////////////////////////////////////////////////////////////////////////////////
void Font_SetBpls(u16* textBpl1, u16* textBpl2);

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void Font_DrawChar(u16 x, u16 y, char c);

////////////////////////////////////////////////////////////////////////////////
// message - test to be written, will end after the next '\f' or '\0'.
// x, y	   - character position to write to.
// return  - pointer to the next message after any '\f' or the end.
////////////////////////////////////////////////////////////////////////////////
const char* Font_DrawMessage(const char* message, u16 x = 0, u16 y = 0);
